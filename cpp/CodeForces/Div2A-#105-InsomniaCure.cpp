#include <iostream>
#include <iostream>
#include <bits/stdc++.h>
#include <string>
#include <numeric>
#include <vector>
#include <cmath>
#include <algorithm>
using namespace std;

				
int main(){
   int k, l, m, n, d; cin >> k >> l >> m >> n >> d;
   int count = 0;

   for(int x=1; x<=d; x++) {
     if(x % k == 0 || x % l == 0 || x%m == 0 || x%n == 0){
        count++;
     }

   }

	

   cout << count << endl;

}
